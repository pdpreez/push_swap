#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*s2;

	i = 0;
	s2 = s;
	if (n != 0)
	{
		while (i < n)
		{
			s2[i] = 0;
			i++;
		}
	}
}
